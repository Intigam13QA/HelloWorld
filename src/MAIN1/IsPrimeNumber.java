package MAIN1;

public class IsPrimeNumber {
    public static void main(String[] args) {

        int x = sum(2, 3, 4, 5);
        System.out.println(x);
        int y = sum(2, 3, 4);
        System.out.println(y);

        System.out.println();



    }

    public static int sum(int a, int b, int c) {
        return a + b + c;

    }

    public static int sum(int a, int b, int c, int d) {
        return a + b + c + d;
    }



    }





